package gulik.demo;

public class Fruit {
    private String name;
    private Integer numberOfSeeds;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getNumberOfSeeds() {
        return numberOfSeeds;
    }

    public void setNumberOfSeeds(Integer n) {
        this.numberOfSeeds = n;
    }
}
